// A linguagem javascript pode exibir informações de diferentes maneiras, como por exemplo:

// 01 - Escrevendo em um elemento HTML com InnerHTML
document.getElementById("idInner").innerHTML = "Texto usando InnerHTML"

// 02 - Escrevendo em uma saida de HTML com document.write()
document.write("Texto usando document.write()")

// 03 - Escrevendo em uma caixa de alerta com window.alert()
window.alert("Texto usando window.alert()")

// 04 - Escrevendo no console do navegador com console.log
console.log("Texto usando console.log")

// 05 - Printando a tela com window.print()
window.print()